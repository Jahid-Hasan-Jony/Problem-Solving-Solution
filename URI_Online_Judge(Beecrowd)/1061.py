bd = input().split()
#------------------------------------------------------------------
d1=int(bd[-1])
x,y,z= map(int,input().split(":"))

#------------------------------------------------------------------
sd = input().split()
d2=int(sd[-1])
x2,y2,z2= map(int,input().split(":"))

#------------------------------------------------------------------
t1=(((d1*24)*60)*60)+((x*60)*60)+(y*60)+z
t2=(((d2*24)*60)*60)+((x2*60)*60)+(y2*60)+z2
s=m=h=d=0
#------------------------------------------------------------------
for i in range(t1,t2):
    s+=1
    if s==60:
        m+=1
        s=0
        if m==60:
            h+=1
            m=0
            if h==24:
                d+=1
                h=0
#------------------------------------------------------------------    
print("{} dia(s)".format(d))
print("{} hora(s)".format(h))
print("{} minuto(s)".format(m))
print("{} segundo(s)".format(s))